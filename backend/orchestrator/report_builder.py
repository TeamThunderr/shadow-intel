# ─── Evidence chain builder ───────────────────────────────────────────────────

def _build_evidence_chain(
    ghost:     AgentResponse | None,
    money:     AgentResponse | None,
    ownership: AgentResponse | None,
    signal:    AgentResponse | None,
    resurface: AgentResponse | None,
) -> list[EvidenceChainStep]:
    """
    Build an ordered evidence chain from all agent results.
    Each chain step summarises the top finding from one agent.
    Steps are only added for agents that ran successfully.
    """
    chain: list[EvidenceChainStep] = []
    step_num = 1

    agent_steps: list[tuple[str, AgentResponse | None]] = [
        ("ghost_tracker",    ghost),
        ("money_trail",      money),
        ("ownership_unwind", ownership),
        ("dark_signal",      signal),
        ("resurface_engine", resurface),
    ]

    for module_name, agent in agent_steps:
        if not agent or agent.status.value == "failed":
            continue

        if agent.evidence:
            top = sorted(agent.evidence, key=lambda e: e.confidence, reverse=True)
            finding = top[0].detail
            sources = list(dict.fromkeys(e.source for e in top[:3]))  # ordered dedup
        else:
            finding = f"No significant findings from {module_name.replace('_', ' ')}."
            sources = [module_name]

        chain.append(EvidenceChainStep(
            step=step_num,
            finding=finding,
            source_module=module_name,
            sources=sources,
            confidence=agent.risk_score,
        ))
        step_num += 1

    return chain


# ─── Public API ───────────────────────────────────────────────────────────────

def build_report(
    entity_id:          str,
    entity_name:        str,
    fingerprint:        EntityFingerprint,
    ghost:              AgentResponse | None,
    money:              AgentResponse | None,
    ownership:          AgentResponse | None,
    signal:             AgentResponse | None,
    resurface:          AgentResponse | None,
    merged_evidence:    list[EvidenceItem],
    unified_confidence: float,
    risk_level:         RiskLevel,
    narrative:          str,
) -> InvestigationReport:
    """
    Assemble the full InvestigationReport from all components and
    render its Markdown representation.

    Returns the complete report ready for session storage and API response.
    """
    evidence_chain = _build_evidence_chain(ghost, money, ownership, signal, resurface)

    report = InvestigationReport(
        entity_id=entity_id,
        entity_name=entity_name,
        unified_confidence=unified_confidence,
        risk_level=risk_level,
        narrative_summary=narrative,
        evidence_chain=evidence_chain,
        ghost_tracker=ghost,
        money_trail=money,
        ownership_unwind=ownership,
        dark_signal=signal,
        resurface_watch=resurface,
    )

    # ── Render Markdown ────────────────────────────────────────────────────
    agents_dict: dict[str, AgentResponse | None] = {
        "ghost_tracker":    ghost,
        "money_trail":      money,
        "ownership_unwind": ownership,
        "dark_signal":      signal,
        "resurface_engine": resurface,
    }
    top_evidence = sorted(merged_evidence, key=lambda e: e.confidence, reverse=True)[:10]

    try:
        template = Template(MARKDOWN_TEMPLATE)
        report.report_markdown = template.render(
            report=report,
            agents=agents_dict,
            top_evidence=top_evidence,
            fingerprint=fingerprint,
        )
    except Exception as exc:
        logger.error(f"Failed to render Markdown report: {exc}")
        # Minimal fallback so the report is never missing its markdown field
        report.report_markdown = f"# Investigation Report\n\n{narrative}"

    logger.info(
        f"Report built | "
        f"entity='{entity_name}' | "
        f"confidence={unified_confidence:.1%} | "
        f"risk={risk_level.value} | "
        f"chain_steps={len(evidence_chain)} | "
        f"evidence_total={len(merged_evidence)}"
    )

    return report
