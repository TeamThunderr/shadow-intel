"""
Investigation report builder.
Assembles the final InvestigationReport from all agent outputs,
the merged evidence set, the unified confidence score, and the
Foundry IQ narrative. Also renders the human-readable Markdown report.
"""

from jinja2 import Template

from shared.schemas import (
    InvestigationReport, AgentResponse, EvidenceItem,
    EvidenceChainStep, RiskLevel, EntityFingerprint,
)
from shared.logger import get_logger

logger = get_logger(__name__)


